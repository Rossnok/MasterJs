// el switch nos permite analizar una variable para analizar distintos casos y que se ejecute un bloque de codigo segun el caso cumplido

var edad = 17;

switch(edad){
    case 18:
     console.log('mayor de edad');
     break;
    case 19:
     console.log('mayor de edad');
     break;
    case 20:
     console.log('mayor de edad');
     break;
    case 21:
     console.log('mayor de edad');
     break;
     default:
        console.log('menor de edad');
        break;
}